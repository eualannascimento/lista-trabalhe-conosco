def main():
    if not os.path.exists(LIST_PATH):
        print(f"Arquivo não encontrado: {LIST_PATH}")
        sys.exit(1)

    with open(LIST_PATH, newline="", encoding="utf-8") as f:
        rows = list(csv.DictReader(f))

    from urllib.parse import urlparse

    def host_of(u: str) -> str:
        try:
            return urlparse(u).netloc.lower()
        except Exception:
            return ""

    existing_urls = {clean_url(r["URL"]).lower() for r in rows}
    existing_hosts = {host_of(r["URL"]) for r in rows if r["URL"]}
    existing_names = {r["Nome da Empresa"].strip().lower() for r in rows}

    # dedup também entradas duplicadas dentro do batch
    seen_batch_name = set()
    batch = []
    for name, seg in COMPANIES:
        k = name.strip().lower()
        if k in existing_names or k in seen_batch_name:
            continue
        seen_batch_name.add(k)
        batch.append((name, seg))

    print(f"Total candidatos a verificar: {len(batch)}")

    session = requests.Session()
    session.headers.update(HEADERS)

    today = date.today().strftime("%Y-%m-%d")
    added = 0
    results: list[tuple[str, str, str]] = []  # (name, seg, url)

    def work(item):
        name, seg = item
        try:
            url = find_url_for(name, session)
        except Exception:
            url = None
        return (name, seg, url)

    workers = int(os.environ.get("WORKERS", "20"))
    with concurrent.futures.ThreadPoolExecutor(max_workers=workers) as ex:
        futures = {ex.submit(work, item): item for item in batch}
        done = 0
        for fut in concurrent.futures.as_completed(futures):
            done += 1
            name, seg, url = fut.result()
            if url:
                h = host_of(url)
                # Hosts compartilhados (carreiras.gupy.io, etc.) precisam de path único
                shared_hosts = {"carreiras.gupy.io", "boards.greenhouse.io",
                                "job-boards.greenhouse.io", "jobs.lever.co",
                                "jobs.kenoby.com"}
                already = (url.lower() in existing_urls or
                           (h not in shared_hosts and h in existing_hosts))
                if not already:
                    results.append((name, seg, url))
                    existing_urls.add(url.lower())
                    existing_hosts.add(h)
            if done % 50 == 0:
                print(f"  progresso {done}/{len(batch)} (+{len(results)})")

    for name, seg, url in results:
        platform = detect_platform(url)
        rows.append(
            {
                "Status da URL": "1",
                "Data de Entrada": today,
                "Nome da Empresa": name,
                "Segmento da Empresa": to_macro(seg, name),
                "Plataforma": platform,
                "URL": url,
            }
        )
        added += 1
        print(f"+ {name} -> {url}")

    with open(LIST_PATH, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=FIELDNAMES, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)

    print(f"\nAdicionadas {added} novas empresas (total {len(rows)})")


if __name__ == "__main__":
    main()
