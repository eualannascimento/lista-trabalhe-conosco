def try_url(session, url: str) -> str | None:
    try:
        r = session.get(url, timeout=6, allow_redirects=True, verify=False)
    except Exception:
        return None
    if not (200 <= r.status_code < 400):
        return None
    final = r.url.lower().rstrip("/")
    if final in ("https://carreiras.gupy.io", "https://portal.gupy.io"):
        return None
    if "solides.jobs" in final and "notfound" in final:
        return None
    if "greenhouse.io" in final and final.endswith("greenhouse.io"):
        return None
    if "kenoby.com" in final and "jobs.kenoby.com" not in final:
        return None
    # Rejeita quando o pattern redireciona para fora do ATS original (ex.: teamtailor → karriar.audi.se).
    src = url.lower()
    if ".teamtailor.com" in src and ".teamtailor.com" not in final:
        return None
    if "lever.co" in src and "lever.co" not in final:
        return None
    if ".gupy.io" in src and ".gupy.io" not in final:
        return None
    return clean_url(r.url)


def find_url_for(name: str, session) -> str | None:
    for url in candidate_urls(name):
        found = try_url(session, url)
        if found:
            return found
    return None


