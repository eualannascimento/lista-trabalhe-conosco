def slugify(name: str) -> str:
    n = unicodedata.normalize("NFKD", name).encode("ascii", "ignore").decode().lower()
    n = "".join(c if c.isalnum() or c == " " else " " for c in n)
    return " ".join(n.split())


def slug_variants(name: str) -> list[str]:
    """Variantes de alta confiança apenas (evita colisões com palavras genéricas)."""
    s = slugify(name)
    if not s:
        return []
    words = s.split()
    no_space = "".join(words)
    dashed = "-".join(words)
    grupo = [w for w in words if w not in {"grupo", "group", "do", "da", "de"}]
    grupo_ns = "".join(grupo)
    grupo_ds = "-".join(grupo)
    variants = {no_space, dashed, grupo_ns, grupo_ds}
    variants.discard("")
    # Slugs muito curtos (<5 chars) só passam se o próprio nome inteiro for curto
    full_short = len(no_space) < 5
    return [v for v in variants if (4 <= len(v) <= 50) or (full_short and v == no_space)]


def candidate_urls(name: str) -> list[str]:
    urls = []
    for s in slug_variants(name):
        urls.extend(
            [
                f"https://{s}.gupy.io",
                f"https://jobs.kenoby.com/{s}",
                f"https://jobs.lever.co/{s}",
                f"https://{s}.teamtailor.com",
            ]
        )
    # dedup keep order
    seen = set()
    out = []
    for u in urls:
        if u not in seen:
            seen.add(u)
            out.append(u)
    return out


